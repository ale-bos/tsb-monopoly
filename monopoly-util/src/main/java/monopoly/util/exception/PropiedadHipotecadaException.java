package monopoly.util.exception;

public class PropiedadHipotecadaException extends Exception {

	private static final long serialVersionUID = -2660718964702052484L;

	public PropiedadHipotecadaException() {
		super();
	}

	public PropiedadHipotecadaException(String message) {
		super(message);
	}

	public PropiedadHipotecadaException(Throwable cause) {
		super(cause);
	}

	public PropiedadHipotecadaException(String message, Throwable cause) {
		super(message, cause);
	}

	public PropiedadHipotecadaException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
